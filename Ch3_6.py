# 6. Write a program with a variable "age" assigned to an integer that
# prints different strings depending on what integer age is.

age = 10

if age < 10:
    print("Wow, you're young! Must be nice!")
elif age < 20:
    print("""Ah the golden years: middle school to high school
              and then off to college!""")
else:
    print("Just quit now. You're old!")
