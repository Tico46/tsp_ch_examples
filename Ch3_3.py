# 3. Write a program that prints a message if a variable is less than or equal to 10,
# another message if the variable is greater than 10 but less than or equal to 25,
# and another message if the variable is greater than 25.

x = 100

if x <= 10:
    print("variable is less than or equal to 10")
elif x > 10 and x <= 25:
    print("Number is between 11 and 25")
else:
    print("Number is greater than 25")
