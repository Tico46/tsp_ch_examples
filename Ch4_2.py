# 3. Write a function that takes three required parameters and two optional parameters.

def sum(x, y, z, a=1, b=1):
    """
    :param: int x, y, z
    :param: int a = 1
    :param: int b = 1
    :return: sum of all numbers
    """
    return x+y+z+a+b

